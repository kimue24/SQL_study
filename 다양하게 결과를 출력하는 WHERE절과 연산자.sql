--별칭을 사용하여 열 이름 출력하기
SELECT ENAME,SAL,SAL*12+COMM AS ANNSAL,COMM
FROM EMP;

--IS NULL 연산자를 사용하여 출력하기

SELECT *
FROM EMP
WHERE COMM IS NULL;

SELECT *
FROM EMP
WHERE MGR IS NOT NULL;

SELECT *
FROM EMP
WHERE SAL > NULL
AND COMM IS NULL;

SELECT * 
FROM EMP
WHERE SAL > NULL
