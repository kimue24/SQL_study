--부서 번호가 30인 데이터만 출력하기
SELECT * FROM EMP
WHERE DEPTNO = 30;

-- 사원번호가 7782인 분만 출력하기
SELECT * FROM EMP
WHERE EMPNO = '7782';

--AND 연산자로 여러 개의 조건식 사용하기
SELECT * FROM EMP
WHERE DEPTNO = 30
AND JOB = 'SALESMAN';

-- 실습 부서번호 30과 사원번호 7499인 사람 출력
SELECT *
FROM EMP
WHERE DEPTNO = 30
AND EMPNO = 7499;

--OR 연산자로 여러개의 출력 조건사용하기
SELECT * FROM EMP
WHERE DEPTNO = 30
OR JOB = 'CLERK';


--산술연산자 곱셈 산술 연산자 사용
SELECT *
FROM EMP
WHERE SAL * 12 = 36000;

--대소 비교 연산자를 사용하여 출력하기
SELECT *
FROM EMP
WHERE SAL >= 3000;

--문자의 대소 비교 연산자로 비교하기
SELECT *
FROM EMP
WHERE ENAME >= 'F';

SELECT *
FROM EMP
WHERE ENAME <= 'FORZ';
-- NOT  연산자를 사용하여 출력하기
SELECT *
FROM EMP
WHERE NOT SAL = 3000;

--IN 연산자
SELECT *
FROM EMP
WHERE JOB IN ('MANAGER','SALESMAN','CLERK');

--등가 비교 연산자와 AND 연산자를 사용하여 출력하기

SELECT * 
FROM EMP
WHERE JOB != 'MANAGER'
AND JOB <> 'SALESMAN'
AND JOB ^='CLERK';

-- IN 연산자와 논리 부정 연산자를 사용하여 출력하기

SELECT *
FROM EMP
WHERE JOB NOT IN ('MANAGER','SALESMAN','CLERK');

SELECT * 
FROM EMP
WHERE DEPTNO IN('10','20');

SELECT *
FROM EMP
WHERE SAL >= 2000
AND SAL <= 3000;

-- 비트윈 AND 
SELECT *
FROM EMP
WHERE SAL BETWEEN 2000 AND 3000;

SELECT *
FROM EMP
WHERE ENAME LIKE 'S%';

SELECT *
FROM EMP
