--테이블 구성 살펴보기
DESC EMP;

DESC DEPT;

DESC SALGRADE;

-- *로 테이블 전체 출력하기
SELECT * FROM EMP;

--열로 쉼표 구분하여 출력하기
SELECT EMPNO,ENAME,DEPTNO
FROM EMP;

--DISTINCT로 열의 중복 제거하기
SELECT DISTINCT DEPTNO
FROM EMP;

--여러 개 열을 명시하여 중복 제거하기
SELECT DISTINCT JOB,DEPTNO
FROM EMP;

--직책 부서 출력하기 ALL 사용
SELECT ALL JOB,DEPTNO
FROM EMP;

--열에 연산식을 사용하여 출력하기
SELECT ENAME,SAL, SAL*12+COMM,COMM
FROM EMP;

--별칭사용
SELECT ENAME,SAL,SAL*12+COMM AS ANNSAL, COMM
FROM EMP;

--EMP 테이블의 모든 열을 급여 기준으로 오름차순 정렬하기
SELECT *
FROM EMP
ORDER BY SAL;

--내림차순 정렬

SELECT *
FROM EMP
ORDER BY SAL;

--내림차순 사용하기

SELECT *
FROM EMP
ORDER BY SAL DESC;

SELECT *
FROM EMP
ORDER BY EMPNO DESC;

--EMP 테이블의 전체열을 부서 번호
SELECT *
FROM EMP
ORDER BY DEPTNO ASC,SAL DESC;

