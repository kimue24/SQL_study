--같은 테이블을 두 번 사용하여 자체 조인하기

SELECT E1.EMPNO, E1.ENAME,E1.MGR,
E2.EMPNO AS MGR_EMPNO,
E2.ENAME AS MGR_ENAME
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO;

--왼쪽 외부 조인 사용하기
SELECT E1.EMPNO, E1.ENAME, E1.MGR,
        E2.EMPNO AS MGR_EMPNO,
        E2.ENAME AS MGR_ENAME
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO(+)
ORDER BY E1.EMPNO;


--오른쪽 외부 조인 사용하기

SELECT E1.EMPNO, E1.ENAME,E1.MGR,
       E2.EMPNO AS MGR_EMPNO,
       E2.ENAME AS MGR_ENAME
FROM EMP E1, EMP E2
WHERE E1.MGR(+) = E2.EMPNO
ORDER BY E1.EMPNO;


--SQL-99 표준 문법으로 배우는 조인,NATURAL JOIN

SELECT E.EMPNO, E.ENAME, E.JOB, E.MGR, E.HIREDATE,E.SAL,E.COMM,
       DEPTNO, D.DNAME, D.LOC
       FROM EMP E NATURAL JOIN DEPT D
       ORDER BY DEPTNO, E.EMPNO;
       
--JOIN ~ USING을 사용하여 조인하기

SELECT E.EMPNO, E.ENAME, E.JOB, E.MGR, E.HIREDATE, E.SAL, E.COMM,
        DEPTNO, D.DNAME, D.LOC
        FROM EMP E JOIN DEPT D USING (DEPTNO)
        WHERE SAL >= 3000
        ORDER BY DEPTNO, E.EMPNO;
        
--JOIN ON으로 등가 조인하기
SELECT E.EMPNO, E.JOB,E.HIREDATE,E.SAL,E.COMM,
        E.DEPTNO,
        D.DNAME,D.LOC
        FROM EMP E JOIN DEPT D ON (E.DEPTNO = D.DEPTNO)
        WHERE SAL <=3000
        ORDER BY E.DEPTNO, EMPNO;
        
 -- 왼쪽 외부 조인을 SQL-99로 작성하기
 
 SELECT E1.EMPNO, E1.ENAME,E1.MGR,
        E2.EMPNO AS MGR_EMPNO,
        E2.ENAME AS MGR_ENAME
        FROM EMP E1 LEFT OUTER JOIN EMP E2 ON(E1.MGR = E2.MGR)
        ORDER BY E1.EMPNO;
        
 -- 오른쪽 외부 조인을 SQL_99로 작성하기
 
 SELECT E1.EMPNO, E1.ENAME,E1.MGR,
        E2.EMPNO AS MGR_EMPNO,
        E2.ENAME AS MGR_ENAME        
        FROM EMP E1 RIGHT OUTER JOIN EMP E2 ON (E1.MGR = E2.MGR)
        ORDER BY E1.EMPNO,MGR_EMPNO;
 
